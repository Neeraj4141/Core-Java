package com.rays.oop.constructor;

public class Shape {
	
	protected String color;
	protected int width;
	
	public String getColor() {
		return this.color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getWidth() {
		return this.width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	

}
